package com.web.model;

public class CategoryVO {
	
	private int category_no;
	private String category_major;
	private String category_minor;
	private String category_link;
	
	public int getCategory_no() {
		return category_no;
	}
	public void setCategory_no(int category_no) {
		this.category_no = category_no;
	}
	public String getCategory_major() {
		return category_major;
	}
	public void setCategory_major(String category_major) {
		this.category_major = category_major;
	}
	public String getCategory_minor() {
		return category_minor;
	}
	public void setCategory_minor(String category_minor) {
		this.category_minor = category_minor;
	}
	public String getCategory_link() {
		return category_link;
	}
	public void setCategory_link(String category_link) {
		this.category_link = category_link;
	}
	
	
}